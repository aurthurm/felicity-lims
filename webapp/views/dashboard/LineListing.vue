                  <th class="px-1 py-1 border-b-2 border-gray-300 text-left leading-4 text-gray-800 tracking-wider">
                    Report Type
                  </th>
                  <th
                    class="px-1 py-1 border-b-2 border-gray-300 text-left text-sm leading-4 text-gray-800 tracking-wider">
                    Generated By
                  </th>
                  <th
                    class="px-1 py-1 border-b-2 border-gray-300 text-left text-sm leading-4 text-gray-800 tracking-wider">
                    Date Generated
                  </th>
                  <th
                    class="px-1 py-1 border-b-2 border-gray-300 text-left text-sm leading-4 text-gray-800 tracking-wider">
                    Period Start
                  </th>
                  <th
                    class="px-1 py-1 border-b-2 border-gray-300 text-left text-sm leading-4 text-gray-800 tracking-wider">
                    Period End
                  </th>
                  <th
                    class="px-1 py-1 border-b-2 border-gray-300 text-left text-sm leading-4 text-gray-800 tracking-wider">
                    Status
                  </th>
                  <th class="px-1 py-1 border-b-2 border-gray-300"></th>
                </tr>
              </thead>
              <tbody class="bg-white">
                <tr v-for="report in reports" :key="report.uid">
                  <td>
                    <input type="checkbox" />
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap border-b border-gray-500">
                    <div class="text-sm leading-5 text-sky-800">
                      {{ report.report_type }}
                    </div>
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap border-b border-gray-500">
                    <div class="text-sm leading-5 text-sky-800">
                      {{ report?.created_by?.first_name }}
                      {{ report?.created_by?.last_name }}
                    </div>
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap border-b border-gray-500">
                    <div class="text-sm leading-5 text-sky-800">
                      {{ report?.created_at }}
                    </div>
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap border-b border-gray-500">
                    <div class="text-sm leading-5 text-sky-800">
                      {{ report?.period_start }}
                    </div>
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap border-b border-gray-500">
                    <div class="text-sm leading-5 text-sky-800">
                      {{ report?.period_end }}
                    </div>
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap border-b border-gray-500">
                    <div class="text-sm leading-5 text-sky-800">{{ report?.status }}</div>
                  </td>
                  <td class="px-1 py-1 whitespace-no-wrap text-right border-b border-gray-500 text-sm leading-5">
                    <button v-show="report.status == 'READY'" @click="downloadReport(report)"
                      class="px-2 py-1 mr-2 border-sky-800 border text-sky-800 rounded-sm transition duration-300 hover:bg-sky-800 hover:text-white focus:outline-none">
                      Download
                    </button>
                    <button v-show="report.status == 'READY'" @click="deleteReport(report)"
                      class="px-2 py-1 mr-2 border-orange-600 border text-orange-600rounded-smtransition duration-300 hover:bg-orange-600 hover:text-white focus:outline-none">
                      Delete
                    </button>
                  </td>
                </tr>
              </tbody>
            </table>
          </div>
          <div v-else class="py-4 pl-4 bg-white">
            <p class="text-orange-600">There are no downloaded reports yet</p>
            <hr />
            <p class="italic">
              Once a download request has been done. Your reports will be appear here
            </p>
          </div>
        </div>
      </template>
    </accordion>

    <accordion>
      <template v-slot:title>Listing Report Request</template>
      <template v-slot:body>
        <div class="my-4">
          <form action="post" class="p-1">
            <div class="grid grid-cols-3 gap-x-4 mb-4">
              <label class="block col-span-1 mb-2">
                <span class="text-gray-700">Report Type</span>
                <select name="report_type" v-model="state.listingForm.report_type" class="form-input mt-1 block w-full">
                  <option value="line_listing" selected>line listing</option>
                </select>
              </label>
              <label class="block col-span-1 mb-2">
                <span class="text-gray-700">Analyses</span>
                <select name="analyses_uids" v-model="state.listingForm.analyses_uids"
                  class="form-input mt-1 block w-full" multiple>
                  <option v-for="service in analysisStore.analysesServices" :key="service.uid" :value="service.uid">
                    {{ service.name }}
                  </option>
                </select>
              </label>
              <label class="block col-span-1 mb-2">
                <span class="text-gray-700">States</span>
                <select name="sample_states" v-model="state.listingForm.sample_states"
                  class="form-input mt-1 block w-full" multiple>
                  <option value=""></option>
