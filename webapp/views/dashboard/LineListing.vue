<script setup lang="ts">
import { reactive, onMounted, defineAsyncComponent } from "vue";
import { REST_BASE_URL } from "@/conf";
import { useAnalysisStore } from "@/stores/analysis";
import { IReportListing } from "@/models/reports";
import useAnalyticsComposable from "@/composables/analytics";
const accordion = defineAsyncComponent(
  () => import("@/components/ui/FelAccordion.vue")
)

const analysisStore = useAnalysisStore();
const { reports, fetchReports, generateReport, deleteReport } = useAnalyticsComposable();

const state = reactive({
  listingForm: {
    report_type: "",
    analyses_uids: [],
    sample_states: [],
    date_column: "",
    period_start: "",
    period_end: "",
  } as IReportListing,
});

onMounted(async () => {
  analysisStore.fetchAnalysesServices({
    first: undefined,
    after: "",
    text: "",
    sortBy: ["name"],
  });
  await fetchReports();
});

const saveListingForm = () => generateReport({ ...state.listingForm });

const downloadReport = (report: any) => {
  const link = document.createElement("a");
  link.download = report.report_type + "-" + report.crated_at;
  link.href = REST_BASE_URL + "/" + report.location;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
};
</script>

<template>
  <div class="bg-white p-2">
    <accordion>
      <template v-slot:title>Downloaded Line Listing Reports</template>
      <template v-slot:body>
        <div class="overflow-x-auto my-4">
          <div v-if="reports?.length > 0"
            class="align-middle inline-block min-w-full shadow overflow-hidden bg-white shadow-dashboard px-2 pt-1 rounded-bl-sm rounded-br-sm">
            <table class="min-w-full">
              <thead>
                <tr>
                  <th class="px-1 py-1 border-b-2 border-gray-300 text-left leading-4 text-gray-800 tracking-wider">
                    <input type="checkbox" />
